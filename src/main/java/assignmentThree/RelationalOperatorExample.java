package assignmentThree;

public class RelationalOperatorExample {
    public static void main(String[] args) {
        int a = 10, b = 20;

        System.out.println("a < b: " + (a < b));
        System.out.println("a > b: " + (a > b));
        System.out.println("a <= b: " + (a <= b));
        System.out.println("a >= b: " + (a >= b));
        System.out.println("a == b: " + (a == b));
        System.out.println("a != b: " + (a != b));

        String text = "Hello";
        System.out.println("text instanceof String: " + (text instanceof String));
    }
}
